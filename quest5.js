// Crie uma variável total inicializada com 0 e uma variável limite com o
// valor 20. Usando um loop while , adicione números inteiros sequenciais (começando
// do 1) a total até que total seja maior ou igual a limite . A cada adição, exiba o
// valor atual de total no console.
let total = 0
let limite = 20

while (total < limite){
    total += 1
    console.log(`Valor total: ${total}`)
}